public class Game {
    private String Name;
    private Double Price;

    public String GetName()
    {
        return this.Name;
    }

    public void SetName(String Name)
    {
        this.Name = Name;
    }

    public Double GetPrice()
    {
        return this.Price;
    }

    public void SetPrice(Double Price)
    {
        this.Price = Price;
    }
}
